num = int(input("Enter a number : "))

# Even or odd
if num % 2 == 0:
    print("Given number {} is Even".format(num))
else:
    print("Given number {} is Odd".format(num))


# divisible by 5 or not
if num % 5 == 0:
    print("Given number {} is divisible by 5".format(num))
else:
    print("Given number {} is not divisible by 5".format(num))


# multiple of 7 or not
if num % 7 == 0:
    print("Given number {} is multiple of 7".format(num))
else:
    print("Given number {} is not multiple of 7".format(num))         
